package main

import (
	"fmt"
	"regexp"
	"strconv"
	"strings"
)

func sliceAtoi(in []string) []int {
	out := make([]int, len(in))
	for i, v := range in {
		out[i], _ = strconv.Atoi(v)
	}
	return out
}

func part1(ss []string) (overlap int) {
	r := regexp.MustCompile("#(\\d+) @ (\\d+),(\\d+): (\\d+)x(\\d+)")
	f := [1000][1000]int{}
	for _, s := range ss {
		m := r.FindStringSubmatch(s)[1:]
		d := sliceAtoi(m)
		for y := d[2]; y < d[2]+d[4]; y++ {
			for x := d[1]; x < d[1]+d[3]; x++ {
				f[y][x]++
			}
		}
	}

	for _, r := range f {
		for _, v := range r {
			if v > 1 {
				overlap++
			}
		}
	}
	return
}

func part2(ss []string) int {
	r := regexp.MustCompile("#(\\d+) @ (\\d+),(\\d+): (\\d+)x(\\d+)")
	f := [1000][1000]int{}
	overlap := map[int]bool{}
	for _, s := range ss {
		m := r.FindStringSubmatch(s)[1:]
		d := sliceAtoi(m)
		for y := d[2]; y < d[2]+d[4]; y++ {
			for x := d[1]; x < d[1]+d[3]; x++ {
				v := f[y][x]
				switch {
				case v == 0:
					f[y][x] = d[0]
				case v > 0:
					f[y][x] = -1
					overlap[v] = true
					overlap[d[0]] = true
				case v < 0:
					overlap[d[0]] = true
				}
			}
		}
	}

	for id := 1; id <= 1+len(ss); id++ {
		if !overlap[id] {
			return id
		}
	}
	return -1
}

