type task struct {
	ps    [][]int
	cs    []int
	count int
}

func (t *task) parse(in string) {
	ss := strings.Split(in, "\n")
	t.ps = make([][]int, 0, len(ss))
	for _, s := range ss {
		sn := strings.Split(s, ",")
		ns := sliceAtoi(sn)
		t.ps = append(t.ps, ns)
	}
}

func manh(p1, p2 []int) int {
	return abs(p1[0]-p2[0]) + abs(p1[1]-p2[1]) + abs(p1[2]-p2[2]) + abs(p1[3]-p2[3])
}

func (t *task) process() {
	t.cs = make([]int, len(t.ps))

	// form constellations
	for i, p := range t.ps {
		if t.cs[i] == 0 {
			t.cs[i] = i + 1
		}
		for j, p2 := range t.ps[i+1:] {
			if manh(p, p2) <= 3 {
				t.cs[i+j+1] = t.cs[i]
			}
		}
	}

	// join constellations
	for {
		stop := true
		for i, p := range t.ps {
			for j, p2 := range t.ps[i+1:] {
				i2 := i + j + 1
				if manh(p, p2) <= 3 && t.cs[i2] != t.cs[i] { // found redundant constellation
					for k := range t.cs {
						if t.cs[k] == t.cs[i2] {
							t.cs[k] = t.cs[i]
						}
					}
					stop = false
				}
			}
		}
		if stop {
			break
		}
	}

	m := map[int]bool{}
	for _, c := range t.cs {
		m[c] = true
	}
	t.count = len(m)
}

func part1(in string) int {
	var t task
	t.parse(in)
	t.process()
	return t.count
}

