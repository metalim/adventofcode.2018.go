type def struct {
	bs    image.Rectangle
	lines []image.Rectangle
}

func parse(in string) def {
	ss := strings.Split(in, "\n")
	r := regexp.MustCompile("(\\w)=(\\d+), (\\w)=(\\d+)..(\\d+)")

	var bs image.Rectangle
	lines := []image.Rectangle{}

	for i, s := range ss {
		fs := r.FindStringSubmatch(s)
		x, _ := strconv.Atoi(fs[2])
		y1, _ := strconv.Atoi(fs[4])
		y2, _ := strconv.Atoi(fs[5])
		var r image.Rectangle
		if fs[1] == "x" {
			r = image.Rect(x, y1, x+1, y2+1)
		} else {
			r = image.Rect(y1, x, y2+1, x+1)
		}
		if i < 0 {
			bs = r
		} else {
			bs = r.Union(bs)
		}
		lines = append(lines, r)
	}
	bs.Min.Y-- // free row at top
	bs.Min.X-- // free col at left
	bs.Max.X++ // free col at right
	return def{bs, lines}
}

const (
	sand    = 0
	isFloor = 1
	isWater = 2
	clay    = isFloor
	flowing = isWater
	settled = isFloor | isWater
)

type field [][]int

func (f field) _print() {
	for _, r := range f {
		s := []rune{}
		for _, v := range r {
			s = append(s, rune(" #|~"[v]))
		}
		_log(string(s))
	}
}

func newField(p def) field {
	f := make(field, 0, p.bs.Dy())
	for y := p.bs.Min.Y; y < p.bs.Max.Y; y++ {
		f = append(f, make([]int, p.bs.Dx()))
	}
	for _, l := range p.lines {
		for y := l.Min.Y; y < l.Max.Y; y++ {
			for x := l.Min.X; x < l.Max.X; x++ {
				f[y-p.bs.Min.Y][x-p.bs.Min.X] = clay
			}
		}
	}
	return f
}

func flow(def def) field {
	f := newField(def)
	next := []image.Point{{500 - def.bs.Min.X, 0}}
	cur := []image.Point{}

	for len(next) > 0 {
		cur, next = next, cur[:0]
		for _, p := range cur {

			if p.Y == len(f)-1 { // flowing to bottom
				f[p.Y][p.X] = flowing
				continue
			}

			if f[p.Y][p.X]&isFloor != 0 { // checking settled cell
				continue
			}

			f[p.Y][p.X] = flowing

			switch f[p.Y+1][p.X] {

			case sand: // no floor yet
				next = append(next, image.Pt(p.X, p.Y+1))
				continue

			case flowing: // flowing below -> ignore
				continue

			case clay, settled: // reached floor
				l, r := -1, -1
				for x := p.X + 1; ; x++ {
					if f[p.Y][x]&isFloor == 0 { // can spread to
						f[p.Y][x] = flowing
						if f[p.Y+1][x]&isFloor == 0 { // has no floor
							next = append(next, image.Pt(x, p.Y+1))
							break
						}
					} else { // blocked
						r = x
						break
					}
				}
				for x := p.X - 1; ; x-- {
					if f[p.Y][x]&isFloor == 0 { // can spread to
						f[p.Y][x] = flowing
						if f[p.Y+1][x]&isFloor == 0 { // has no floor
							next = append(next, image.Pt(x, p.Y+1))
							break
						}
					} else { // blocked
						l = x
						break
					}
				}
				if l >= 0 && r >= 0 {
					for x := l + 1; x < r; x++ {
						f[p.Y][x] = settled
						if f[p.Y-1][x]&isWater != 0 {
							next = append(next, image.Pt(x, p.Y-1))
						}
					}
				}
			}
		}
	}
	return f[1:] // skip the first row, remember?
}

func process(def def) field {
	f := flow(def)
	// f._print() // uncomment to see the image. Make sure your font is small
	return f
}

func part1(def def, f field) (out int) {
	for _, r := range f {
		for _, v := range r {
			if v&isWater != 0 {
				out++
			}
		}
	}
	return
}

func part2(def def, f field) (out int) {
	for _, r := range f {
		for _, v := range r {
			if v == settled {
				out++
			}
		}
	}
	return
}

